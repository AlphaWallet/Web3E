/**
 * @brief Get the Certificate from chainId
 * 
 * If the certificate is not here, the Web3 connection uses unsecured mode. This is fine for testnets.
 * Add certificates here if you use the mainnet and please submit a PR back to github/AlphaWallet/Web3E
 * To help the community.
 * 
 * @param chainId 
 * @return const char* 
 */
const char* getCertificate(long long chainId, const char* infura_key)
{
    switch (chainId)
    {
    case MAINNET_ID:
        if (infura_key != 0)
        {
            return infura_ca_cert;
        }
        else
        {
            return linkpool_mainnet_cert;
        }
    case POLYGON_ID:
    case KOVAN_ID:
    case MUMBAI_TEST_ID:
    case OPTIMISTIC_MAIN_ID:
    case OPTIMISTIC_TEST_ID:
    case ARBITRUM_MAIN_ID:
    case ARBITRUM_TEST_ID:
    case PALM_ID:
    case PALM_TEST_ID:
        if (infura_key != 0)
        {
            return infura_ca_cert;
        }
        else
        {
            return 0; //currently no cert
        }
        
    case RINKEBY_ID:
    case GOERLI_ID:
        if (infura_key != 0)
        {
            return infura_ca_cert;
        }
        else
        {
            return linkpool_cert;
        }
    case KLAYTN_ID:
    case KLAYTN_BOABAB_ID:
        return klaytn_cert;
    case IOTEX_MAINNET_ID:
    case IOTEX_TESTNET_ID:
        return iotex_cert;
    default:
        return 0;
    }
}

